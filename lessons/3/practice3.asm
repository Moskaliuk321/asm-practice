; ==========================================
; Практична робота №3
; Завдання: Конвертація числа в рядок (int to string)
; ==========================================

section .text
    global _start

_start:
    ; --- logic: ініціалізація тестового числа ---
    mov eax, 123456   

    ; --- memory: підготовка буфера в стеку ---
    sub esp, 16       ; виділяємо 16 байт під рядок
    mov edi, esp      ; вказівник на початок буфера
    add edi, 15       ; переходимо в самий кінець
    mov ecx, 0        ; лічильник символів

.loop_div:
    ; --- math: алгоритм конвертації ---
    xor edx, edx      ; обнуляємо залишок перед діленням
    mov ebx, 10
    div ebx           ; EAX / 10. Частка в EAX, залишок в EDX
    
    ; --- parse: перетворення цифри в ASCII символ ---
    add dl, '0'       ; додаємо 48 (ASCII код нуля)
    dec edi           ; зсуваємо вказівник вліво
    mov [edi], dl     ; записуємо символ
    inc ecx           ; збільшуємо довжину рядка
    
    ; --- loops: цикл до повного розбору числа ---
    test eax, eax
    jnz .loop_div

    ; --- I/O: системний виклик write для виводу числа ---
    mov eax, 4        ; sys_write
    mov ebx, 1        ; stdout
    mov edx, ecx      ; довжина рядка (кількість цифр)
    mov ecx, edi      ; адреса першої цифри
    int 0x80

    ; --- I/O: вивід символу нового рядка (\n) ---
    push 0xA          ; кладемо код перенесення рядка в стек
    mov eax, 4
    mov ebx, 1
    mov ecx, esp
    mov edx, 1
    int 0x80

    ; --- logic: завершення програми ---
    mov eax, 1        ; sys_exit
    xor ebx, ebx      ; код повернення 0
    int 0x80